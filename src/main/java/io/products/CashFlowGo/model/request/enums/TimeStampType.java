package io.products.CashFlowGo.model.request.enums;

/**
 * Enums representing different types of timestamp intervals.
 * This enum defines predefined time intervals such as the last 24 hours, last week, last month,
 * and a custom interval that can be specified by the user.
 */
public enum TimeStampType {
    LAST_24_HOUR, LAST_WEEK, LAST_MONTH, CUSTOM
}
